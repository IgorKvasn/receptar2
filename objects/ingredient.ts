export enum IngredientUnit {
  G = 'g',
  ML = 'ml',
  PCS = 'pcs',
  TBLSP = 'tblsp',
  TEASP = 'teasp',
  HANDFUL = 'handful'
}

export class Ingredient {
  name: string;
  amount: number;
  unit: IngredientUnit;
  selected?: boolean = false;

  copy(): Ingredient {
    return JSON.parse(JSON.stringify(this));
  }
}
